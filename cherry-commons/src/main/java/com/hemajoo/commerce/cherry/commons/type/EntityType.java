package com.hemajoo.commerce.cherry.commons.type;

/**
 * Enumeration representing the several possible entity types.
 * @author <a href="mailto:christophe.resse@gmail.com">Christophe Resse</a>
 * @version 1.0.0
 */
public enum EntityType
{
    /**
     * Unspecified entity type.
     */
    UNSPECIFIED,

    /**
     * Media entity type.
     */
    MEDIA,

    /**
     * Document type.
     */
    DOCUMENT,

    /**
     * Person entity type.
     */
    PERSON,

    /**
     * Email address entity type.
     */
    EMAIL_ADDRESS,

    /**
     * Postal address entity type.
     */
    POSTAL_ADDRESS,

    /**
     * Phone number entity type.
     */
    PHONE_NUMBER,

    /**
     * Account entity type.
     */
    ACCOUNT,

    /**
     * Customer entity type.
     */
    CUSTOMER,

    /**
     * Organization entity type.
     */
    ORGANIZATION,

    /**
     * Company entity type.
     */
    COMPANY,

    /**
     * Department entity type.
     */
    DEPARTMENT,

    /**
     * Shop entity type.
     */
    SHOP,

    /**
     * Employee entity type.
     */
    EMPLOYEE,
}
